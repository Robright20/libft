#include "libft.h"

char	*ft_itoa(int n)
{
	if (n == MIN_INT)
	{
		ft_itoa(-214748364);
		n = 8;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n < 10)
		ft_putchar('0' + n);
	else if (n >= 10)
	{
		ft_itoa(n / 10);
		ft_itoa(n % 10);
	}
	return ("");
}
