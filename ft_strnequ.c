#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	if ((*s1 == *s2))
	{
		if ((*(s1 + 1) || *(s2 + 1)) && (n - 1))
			return (ft_strnequ((s1 + 1), (s2 + 1), n - 1));
		else
			return (1);
	}
	return (0);
}
